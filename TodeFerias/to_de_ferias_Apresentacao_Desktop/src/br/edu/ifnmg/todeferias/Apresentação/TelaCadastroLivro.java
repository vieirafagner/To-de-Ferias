    public void recuperaCampos() throws ErroValidacao{
        String titulo = txtTitulo.getText().trim();
        if(!titulo.equals(""))
            entidade.setTitulo(titulo);
        
        String autor = txtAutor.getText().trim();
        if(!autor.equals(""))
            entidade.setAutor(autor);
        
        Integer qtdPag = Integer.parseInt(txtQtdPag.getText());
        if(!qtdPag.equals(""))
            entidade.setQtd_pag(qtdPag);
        
        String resumo = txtResumo.getText().trim();
        if(!resumo.equals(""))
            entidade.setResumo(resumo);
    }
 
